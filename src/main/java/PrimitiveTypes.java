public class PrimitiveTypes {

    public static void main(String[] args) {

        // Integer
        int intValue = 123;
        System.out.println(intValue);

        // Integer size range
        int intMinValue = Integer.MIN_VALUE;
        int intMaxValue = Integer.MAX_VALUE;
        System.out.println("Integer primitive type has the size between " + intMinValue + " and " + intMaxValue);


        // Byte
        byte byteValue = 1;
        System.out.println(byteValue);

        // Byte size range
        byte byteMinValue = Byte.MIN_VALUE;
        byte byteMaxValue = Byte.MAX_VALUE;
        System.out.println("Byte primitive type has the size between " + byteMinValue + " and " + byteMaxValue);


        // Short
        short shortValue = 1;
        System.out.println(shortValue);

        short shortMinValue = Short.MIN_VALUE;
        short shortMaxValue = Short.MAX_VALUE;
        System.out.println("Short primitive type has the size between " + shortMinValue + " and " + shortMaxValue);


        // Char
        char charValue = 'a';
        System.out.println(charValue);

        // Char size range
        char charMinValue = Character.MIN_VALUE;
        char charMaxValue = Character.MAX_VALUE;
        System.out.println("Char primitive type has the size between " + charMinValue + " and " + charMaxValue);

        // Long
        long longValue = 1;
        System.out.println(longValue);

        // Long size range
        long longMinValue = Long.MIN_VALUE;
        long longMaxValue = Long.MAX_VALUE;
        System.out.println("Long primitive type has the size between " + longMinValue + " and " + longMaxValue);


        // Float
        float floatValue = 1;
        System.out.println(floatValue);

        // Float size range
        float floatMinValue = Float.MIN_VALUE;
        float floatMaxValue = Float.MAX_VALUE;
        System.out.println("Float primitive type has the size between " + floatMinValue + " and " + floatMaxValue);


        // Double
        double doubleValue = 1;
        System.out.println(doubleValue);

        // Double size range
        double doubleMinValue = Double.MIN_VALUE;
        double doubleMaxValue = Double.MAX_VALUE;
        System.out.println("Double primitive type has the size between " + doubleMinValue + " and " + doubleMaxValue);


        // Boolean
        boolean booleanValue = true;
        System.out.println(booleanValue);

        // Boolean size range
        boolean booleanMinValue = Boolean.FALSE;
        boolean booleanMaxValue = Boolean.TRUE;
        System.out.println("Boolean primitive type has the size between " + booleanMinValue + " and " + booleanMaxValue);


    }

}
